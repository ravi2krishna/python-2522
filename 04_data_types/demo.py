# Data Types

# Numeric Types 
data = 10
print(type(data))
data = -10
print(type(data))

data = 10.0
print(type(data))

# data = 3+i5
j = 10
data = 3+5j
print(type(data)) 

# Text data 
data = "good morning"
print(type(data)) 

# Boolean Data
data = True
print(type(data)) 

# None Type 
data = None
print(type(data)) 

# List Type
data = [10,20,30,40,50]
print(type(data)) 

# Tuple Type
data = (10,20,30,40,50)
print(type(data)) 

# Set Type
data = {10,20,30,40,50}
print(type(data)) 

# Dictionary Type
data = {"name":"name is used to identify some entity","age":"represents how old an entity is"}
data = {"name":"ravi","age":25}
print(type(data)) 

# Custom Data Type
class Student:
    pass 

data = Student()
print(type(data))
